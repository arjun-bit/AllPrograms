package a1;

public class Alpha_Static {

//	Overloading for Static Method
	static void Demo()
	{
		System.out.println("Hiii!!!");
	}
	
	static void Demo(int i)
	{
		System.out.println("Hello!!!");
	}
	
	public static void main(String[]args)
	{
		
		Demo();
		Demo(5);
		
		
	}
}
