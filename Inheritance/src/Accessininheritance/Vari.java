package Accessininheritance;

public class Vari {

	public int a= 2;
	int b = 13;
	private int c = 14;
	protected int d = 15;
public static void main(String[] args)
	{
		Vari x=  new Vari();
		System.out.println(x.a);
		System.out.println(x.b);
		System.out.println(x.c);
		System.out.println(x.d);	
	}
}
